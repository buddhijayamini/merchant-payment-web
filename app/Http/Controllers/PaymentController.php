     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $query = Payment::query();
        $query = $this->getFilters($query);
        $query = $query->paginate(10);
        return view('payment.payment_details', compact('query'));
    }


    private function getFilters($query)
    {
        if(request()->has('district') && request('district') != null){
            $query = $query->where('district','LIKE','%'.request('district').'%');
        }
        if(request()->has('postal_code') && request('postal_code') != null){
            $query = $query->where('postal_code','LIKE','%'.request('postal_code').'%');
        }
        if(request()->has('start') && request()->has('end')){
            $query = $query->whereBetween('payment_date',array(request('start'),request('end')));
        }
        return $query;
    }
